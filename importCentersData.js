// Function to map Arabic field names to schema fields
function mapToSchema(center) {
    return {
        name: center['اسم المركز'],
        phone: center['التلفون']?.toString() || '',
        address: center['العنوان '] || '',
        locationArea: center['موقع المنطقه'] || '',
        facebookLink: center['الموقع ']?.includes('facebook.com') ? center['الموقع '] : '',
        mapLink: !center['الموقع ']?.includes('facebook.com') ? center['الموقع '] : ''
    };
}

async function importCenters() {
    try {
        // Connect to MongoDB
        await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/ministry_youth_sports');
        console.log('✅ Connected to MongoDB');

        // Clear existing centers
        await Center.deleteMany({});
        console.log('🧹 Cleared existing centers');

        // Process and validate centers
        const mappedCenters = centersData
            .map(center => mapToSchema(center))
            .filter(center => {
                if (!center.name || typeof center.name !== 'string' || center.name.trim() === '') {
                    console.warn(`⚠️  Skipping center with missing name`);
                    return false;
                }
                return true;
            });

        console.log(`📊 Found ${centersData.length} total centers, ${mappedCenters.length} are valid`);

        if (mappedCenters.length > 0) {
            // Insert new centers
            const createdCenters = await Center.insertMany(mappedCenters);
            console.log(`✅ Successfully imported ${createdCenters.length} centers`);
        } else {
            console.log('❌ No valid centers to import');
        }

        // Close the connection
        await mongoose.connection.close();
        console.log('🔌 MongoDB connection closed');
    } catch (error) {
        console.error('❌ Error importing centers:', error);
        process.exit(1);
    }
}

